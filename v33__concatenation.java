public class v33__concatenation {
    public static void main(String[] args) {
        String s1 = "Sabbir";
        String s2 = "Hasanat";
        String full = s1+ " "+s2;

        System.out.println(full);

        System.out.println("My id is " + 2 + 5);
        System.out.println("My id is " + (2 + 5));

        System.out.println("Sabbir".concat(" Hasanat").concat(" - 1007"));
    }
}
